  <script>
    

    $(document).ready(function(){
  
    if(!md.mobile()){
      VANTA.WAVES({
        el: "#cover-bg",
        mouseControls: true,
        touchControls: true,
        minHeight: 200.00,
        minWidth: 200.00,
        scale: 1.00,
        scaleMobile: 1.00,
        color: 0xff430a
      });
    }
    

    $('.animated-gradient-bg').mousemove(function(event) {
      windowWidth = $(window).width();
      windowHeight = $(window).height();
      
      mouseXpercentage = Math.round(event.pageX / windowWidth * 100);
      mouseYpercentage = Math.round(event.pageY / windowHeight * 100);
      
      $(this).css('background', 'radial-gradient(at ' + mouseXpercentage + '% ' + mouseYpercentage + '%, #ff1353, #ffa90a 100%)');
    });

    $('.animated-gradient-bg').mouseleave(function(event) {
      $(this).css('background', 'transparent');
    });



    function random_slide(){
      var slider_count = $('.portfolio-thumb-container').length;

      if(slider_count == 0){
        return;
      }

      var rnd_index = parseInt(Math.random() * slider_count);
      var target = $('.portfolio-thumb-container:eq(' + rnd_index + ')');
      var target_index = parseInt($(target).attr('data-index'));
      var slide_count = $(target).find('.portfolio-thumb').length;

      if(slide_count == 0){
        return;
      }


      target_index++;
      if(target_index >= slide_count){
        target_index = 0;
      }


      var effects = ['slideInRight', 'slideInLeft', 'slideInUp', 'slideInDown'];
      var rnd_effect = effects[parseInt(Math.random() * 4)];

      $(target).attr('data-index', target_index);
      $('.portfolio-thumb-container').removeClass('active');
      $(target).addClass('active');
      $(target).find('.portfolio-thumb').css('z-index', '0');
      $(target).find('.portfolio-thumb.active').css('z-index', '1').removeClass('animated slideInRight slideInLeft slideInUp slideInDown');
      $(target).find('.portfolio-thumb').removeClass('active');

      var slide = $('.portfolio-thumb-container.active .portfolio-thumb:eq(' + target_index.toString() + ')');
      $(slide).addClass('animated ' + rnd_effect + ' active');
      $(slide).css('z-index', '2');

    }



    
    if(!md.mobile()){
      setInterval( random_slide, 1500 );
      setInterval( random_slide, 1333 );
    }

    
    
  });


  </script>


